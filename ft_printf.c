#include "printf.h"

int		ft_printf(const char *str, ...)
{
	int		i;

	while (str[i])
	{
		if (str[i] == '%')
		{
			ft_putchar((char)str[i]);
			i++;
		}
		else
			break ;
	}
	return (0);
}
